import SwiftUI

public extension Color {
    // Light theme
    static let themeSoftLavender = Color(red: 224/255, green: 220/255, blue: 237/255)
    static let themeBlushPink    = Color(red: 249/255, green: 223/255, blue: 226/255)
    static let themeSageGreen    = Color(red: 207/255, green: 225/255, blue: 210/255)
    static let themeBeige        = Color(red: 245/255, green: 240/255, blue: 230/255)

    // Dark theme (deeper + softer)
    static let themeMidnight     = Color(red: 13/255,  green: 13/255,  blue: 15/255)   // #0D0D0F
    static let themeForest       = Color(red: 26/255,  green: 42/255,  blue: 36/255)   // #1A2A24
    static let themeMoodyPlum    = Color(red: 42/255,  green: 31/255,  blue: 47/255)   // #2A1F2F
}
